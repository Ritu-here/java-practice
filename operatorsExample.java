public class operatorsExample {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        System.out.println("Arithmetic operator");

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a % b);



        double x = 23.3;
        double y = 12.3;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
    }
}
